// scripts/set-build-meta.mjs

import { execSync } from 'node:child_process';
import { mkdirSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';

const outputPath = resolve('src/lib/build-meta.ts');

function safeExec(command, fallback = 'unknown') {
  try {
    return execSync(command, { stdio: ['ignore', 'pipe', 'ignore'] }).toString().trim() || fallback;
  } catch {
    return fallback;
  }
}

const commit = safeExec('git rev-parse --short HEAD');
const generatedAt = new Date().toISOString();

const content = `// src/lib/build-meta.ts
// Auto-generated by scripts/set-build-meta.mjs. Do not edit manually.

export const BUILD_META = {
  commit: '${commit}',
  generatedAt: '${generatedAt}',
} as const;
`;

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, content, 'utf8');

console.log(`Generated build metadata: ${commit}`);